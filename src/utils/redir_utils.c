#include "parsing.h"

int	is_redir(char *arg)
{
	if (ft_strcmp(arg, ">") && ft_strcmp(arg, ">>")
		&& ft_strcmp(arg, "<") && ft_strcmp(arg, "<<"))
		return (0);
	return (1);
}
